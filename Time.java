public class Time {

    int min;
    int sec;
    String minO;
    String secO;    

    public Time(){
        min = 0;
        sec = 0;
        minO = "0";
        secO = "0";
    }

    public int getMin(){
        return min;
    }

    public int getSec(){
        return sec;
    }
    
}
